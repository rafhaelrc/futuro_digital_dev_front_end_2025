// --- Parte 1: Verificando a expressão original ---

console.log("--- Expressão Original ---");

// A divisão (20 / 2) acontece primeiro
let resultado1 = 10 + 20 / 2;

// Exibe o resultado no console para verificação
console.log("O resultado de '10 + 20 / 2' é:", resultado1);


// Adiciona uma linha em branco para separar as partes
console.log("\n");


// --- Parte 2: Modificando a expressão com parênteses ---

console.log("--- Expressão Modificada com Parênteses ---");

// Os parênteses forçam a adição (10 + 20) a acontecer primeiro
let resultado2 = (10 + 20) / 2;

// Exibe o novo resultado
console.log("O resultado de '(10 + 20) / 2' é:", resultado2);